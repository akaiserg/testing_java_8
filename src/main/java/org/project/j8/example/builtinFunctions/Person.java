package org.project.j8.example.builtinFunctions;

public class Person {

    private String fName;
    private String lName;

    public Person (String fName, String lName){
        this.fName = fName;
        this.lName = lName;
    }

    public String getfName() {
        return fName;
    }

    public String getlName() {
        return lName;
    }
}
